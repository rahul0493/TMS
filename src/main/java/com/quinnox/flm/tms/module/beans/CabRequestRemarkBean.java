package com.quinnox.flm.tms.module.beans;

import java.io.Serializable;

public class CabRequestRemarkBean implements Serializable {

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 7291413239295382346L;
	private String comment;
	private String action;
	private int cabRequestId;
	
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public int getCabRequestId() {
		return cabRequestId;
	}
	public void setCabRequestId(int cabRequestId) {
		this.cabRequestId = cabRequestId;
	}
	
	
}
