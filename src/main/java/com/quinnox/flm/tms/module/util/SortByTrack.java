package com.quinnox.flm.tms.module.util;

import java.util.Comparator;

import com.quinnox.flm.tms.module.shiftplanner.bean.StatusBean;

public class SortByTrack implements Comparator<StatusBean> {

	@Override
	public int compare(StatusBean o1, StatusBean o2) {
		
		return o1.getTrackId() - o2.getTrackId();
	}

}
