package com.quinnox.flm.tms.module.beans;

import java.io.Serializable;
import java.util.List;

/**
 * @author ImranGS
 *
 */

public class VendorBean implements Serializable{
	
	private static final long serialVersionUID = -1429212665477057549L;
	private Integer vendorId;
	private String travelsName;
	private String ownerName;
	private String officeAddress;
	private String officeNumber;
	private  List<VendorSpocBean> vendorSpocs;
	private double rateWithoutEscort;
	private double rateWithEscort;
	
	public Integer getVendorId() {
		return vendorId;
	}
	public void setVendorId(Integer vendorId) {
		this.vendorId = vendorId;
	}
	public String getTravelsName() {
		return travelsName;
	}
	public void setTravelsName(String travelsName) {
		this.travelsName = travelsName;
	}
	public String getOfficeAddress() {
		return officeAddress;
	}
	public void setOfficeAddress(String officeAddress) {
		this.officeAddress = officeAddress;
	}
	public String getOfficeNumber() {
		return officeNumber;
	}
	public void setOfficeNumber(String officeNumber) {
		this.officeNumber = officeNumber;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public List<VendorSpocBean> getVendorSpocs() {
		return vendorSpocs;
	}
	public void setVendorSpocs(List<VendorSpocBean> vendorSpocs) {
		this.vendorSpocs = vendorSpocs;
	}
	public double getRateWithoutEscort() {
		return rateWithoutEscort;
	}
	public void setRateWithoutEscort(double rateWithoutEscort) {
		this.rateWithoutEscort = rateWithoutEscort;
	}
	public double getRateWithEscort() {
		return rateWithEscort;
	}
	public void setRateWithEscort(double rateWithEscort) {
		this.rateWithEscort = rateWithEscort;
	}
	 
}
